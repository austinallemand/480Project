    /*********
    Bit shift encryption for SSNs (similiar to a Ceasear shift)
    *********/
    private int encrypt(int passedSSN){
        int shiftOne = 3;
        int encryptedSSN = passedSSN;
//
        encryptedSSN = encryptedSSN >> shiftOne;

        return encryptedSSN;
    }
    
